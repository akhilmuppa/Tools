package com.osi.tools.bean;

public class SettingBean {


	private int id;
	private String blockopt;
	private String callopt;
	private String Intstorage;
	public String getIntstorage() {
		return Intstorage;
	}
	public void setIntstorage(String intstorage) {
		Intstorage = intstorage;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBlockopt() {
		return blockopt;
	}
	public void setBlockopt(String blockopt) {
		this.blockopt = blockopt;
	}
	public String getCallopt() {
		return callopt;
	}
	public void setCallopt(String callopt) {
		this.callopt = callopt;
	}




}
